
class SlideContent {
  /// Path to the illustrative image asset (e.g. 'assets/images/onboarding_1.png').
  final String image;

  /// Title text for the slide.
  final String title;

  /// Description text for the slide.
  final String description;

  const SlideContent({
    required this.image,
    required this.title,
    required this.description,
  });
}
